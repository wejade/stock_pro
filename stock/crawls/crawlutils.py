# 装饰器，用于统计运行时间
def compute_times(func):
    def inside(*args, **kwargs):
        begin = datetime.datetime.now()
        result = func(*args, **kwargs)
        end = datetime.datetime.now() - begin
        print('函数<%s>共耗时-%s-秒' % (func.__name__, end.seconds))
        return result
    
    return inside


# 交易时间，每10秒一次
def get_trade_times(second=10):
    '''
    交易时间，9:30-11:30,13:00-15:00
    每10秒生成一个时间对象
    :return:返回pandas._libs.tslib.Timestamp对象，此对象是datetime.datetime的子类
    '''
    delta = datetime.timedelta(seconds=second)
    # 生成交易时间，9:30-11:30,13:00-15:00
    now = datetime.datetime.now()
    am_start = datetime.datetime(year=now.year, month=now.month, day=now.day, hour=9, minute=30, second=0)
    am_end = datetime.datetime(year=now.year, month=now.month, day=now.day, hour=11, minute=30, second=0)
    pm_start = datetime.datetime(year=now.year, month=now.month, day=now.day, hour=13, minute=00, second=0)
    pm_end = datetime.datetime(year=now.year, month=now.month, day=now.day, hour=15, minute=00, second=0)
    
    am_range = pd.date_range(am_start, am_end, freq=str(second) + 'S')
    pm_range = pd.date_range(pm_start, pm_end, freq=str(second) + 'S')
    date_range = list(am_range) + list(pm_range)
    
    return date_range


# 获取所有股票及其对应的市场代码
def get_stocks_all_by_market(market=True):
    with connection.cursor() as cursor:
        cursor.execute('select symbol from stock_stockbasicinfo')
        codes = cursor.fetchall()
        codes = [code[0] for code in codes]
        if not market:
            return codes
        codes = map(lambda code: (1, code) if code.startswith('6') else (0, code), codes)
        return list(codes)


